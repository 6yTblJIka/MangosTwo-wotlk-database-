    -- Source: https://github.com/ACID-Scripts/WOTLK/commit/c344dd0
-- Twilight Thug - Script Update.
DELETE FROM `creature_ai_scripts` WHERE creature_id = 2339;
INSERT INTO `creature_ai_scripts` VALUES
