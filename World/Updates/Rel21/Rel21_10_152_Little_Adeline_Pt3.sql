DELETE FROM `gossip_menu_option` WHERE `menu_id` = 10649;
INSERT INTO `gossip_menu_option` (`menu_id`, `option_text`, `option_id`, `npc_option_npcflag`, `action_menu_id`, `action_script_id`) VALUES 
